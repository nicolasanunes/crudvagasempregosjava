    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        Empresa empresa = new Empresa();
        empresa.setNome(campoNome.getText());
        empresa.setCnpj(campoCnpj.getText());
        empresa.setEmail(campoEmail.getText());
        empresa.setTelefone(campoTelefone.getText());
        empresa.setCidade(campoCidade.getText());
        empresa.setEndereco(campoEndereco.getText());
        
        DAOEmpresa insereDados = new DAOEmpresa();
        if(insereDados.insereEmpresa(empresa)) {
            JOptionPane.showMessageDialog(null, "Registro cadastrado com sucesso!");
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar registro, tente novamente.");
            limparCampos();
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

