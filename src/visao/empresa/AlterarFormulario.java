    private void botaoAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAlterarActionPerformed
        Empresa empresa = new Empresa();
        empresa.setId(Integer.parseInt(campoId.getText()));
        empresa.setNome(campoNome.getText());
        empresa.setCnpj(campoCnpj.getText());
        empresa.setEmail(campoEmail.getText());
        empresa.setTelefone(campoTelefone.getText());
        empresa.setCidade(campoCidade.getText());
        empresa.setEndereco(campoEndereco.getText());
        
        DAOEmpresa atualizar = new DAOEmpresa();
        if(atualizar.alteraEmpresa(empresa)) {
            JOptionPane.showMessageDialog(null, "Registro Atualizado com Sucesso.");
            dispose();
            new Alterar().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao Atualizar registro");
            dispose();
            new Alterar().setVisible(true);
        }
    }//GEN-LAST:event_botaoAlterarActionPerformed
 
